    Locale locale,
    String key, [
    Map<String, String> params = const {},
  ]) {
    final code = locale.languageCode;
    var text = _strings[code]?[key] ?? _strings['en']?[key] ?? key;
    for (final entry in params.entries) {
      text = text.replaceAll('{${entry.key}}', entry.value);
    }
    return text;
  }

